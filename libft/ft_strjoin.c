#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	dst_size;

	dst_size = ft_strlen(s1) + ft_strlen(s2) + 1;
	str = (char *)ft_calloc(dst_size, 1);
	if (!str)
		return (NULL);
	ft_strlcpy(str, s1, ft_strlen(s1) + 1);
	ft_strlcat(str, s2, dst_size);
	return (str);
}
